CREATE DATABASE IF NOT EXISTS museo_stacksquad;
USE museo_stacksquad;

CREATE TABLE Tema (
    codice INT AUTO_INCREMENT PRIMARY KEY,
    descrizione VARCHAR(255) NOT NULL
);

CREATE TABLE Sala (
    numero INT PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    superficie DECIMAL(8,2) NOT NULL,
    temaSala INT,
    FOREIGN KEY (temaSala) REFERENCES Tema(codice) ON DELETE SET NULL
);

CREATE TABLE Autore (
    codice INT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    cognome VARCHAR(100) NOT NULL,
    nazione VARCHAR(100) NOT NULL,
    dataNascita DATE NOT NULL,
    tipo ENUM('vivo', 'morto') NOT NULL,
    dataMorte DATE NULL,
    pathImmagine VARCHAR(512) NULL
);

CREATE TABLE Opera (
    codice INT AUTO_INCREMENT PRIMARY KEY,
    autore INT NOT NULL,
    titolo VARCHAR(255) NOT NULL,
    annoAcquisto INT NOT NULL,
    annoRealizzazione INT NOT NULL,
    tipo ENUM('Quadro', 'Scultura') NOT NULL,
    espostaInSala INT,
    pathImmagine VARCHAR(512) NULL,
    FOREIGN KEY (autore) REFERENCES Autore(codice) ON DELETE CASCADE,
    FOREIGN KEY (espostaInSala) REFERENCES Sala(numero) ON DELETE SET NULL
);

